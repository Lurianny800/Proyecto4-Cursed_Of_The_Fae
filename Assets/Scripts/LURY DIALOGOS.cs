FAUNA:
Acerquense, mis pupilos. Una urgencia nos atosiga y no hay tiempo que perder.

La Doncella de las Flores, Persefone, ha sido raptada nuevamente al Reino de las Sombras por Hades. 

Fui testigo de como sus gritos de auxilio hundian el corazon del bosque.

Sin el regreso de Persefone a la superficie, la primavera no puede florecer y la naturaleza se marchita dia a dia. 

Es una amenaza para nuestro mundo que no puedo permitir.
LUMI: He sentido el bosque enfermando a mi alrededor. Los arboles agonizan, el aire es viciado.

LYRA: Esta es la mision que esperabamos! Yo conozco muy bien la historia de Persefone.

LUMI: Es la Doncella de la que Hades se enamoro mientras recogia flores en los campos. 

Cuando su madre, Demeter, la busco desesperada, su tristeza y rabia provocaron que el mundo se cubriera de nieve y hielo.

APOLO: Si! Y cuando Persefone regresa cada primavera de las profundidades, la naturaleza y la vida vuelve a florecer!

LYRA: Exacto! Hades la tiene cautiva en su oscuro reino subterraneo. 

¡Debemos rescatarla antes de que sea tarde!

THADEUS:  "No podemos fallar en esta mision. 

Toda vida en este plano depende de que traigamos de vuelta a Persefone."

APOLO: Estoy listo para la batalla! ¡Por la naturaleza!

FAUNA: Mis valientes pupilos, han esperado tanto por este dia. 

Confio en que traeran de vuelta el equilibrio con valentia y sabiduria. 

Recuerden mis lecciones sobre la empatia, la fuerza interior y la conviccion de sus ideales. 

 Pero tengan cuidado, pues los caminos al inframundo estan plagados de peligros y tentaciones que pondran a prueba sus almas.

FAUNA: Vayan ahora, pupilos mios. La balanza de este mundo descansa en sus manos.

LUMI: El bosque encantado por el que vamos a viajar solia ser un paraiso…

 rebosante de vida salvaje, magia de las Fae y belleza sin igual. 

Pero desde que Persefone fue capturada por Hades, su ausencia ha ido drenando el verdor de estas tierras...

LYRA: Puedo sentir la agonia fluyendo por las venas del bosque. 

Es como si se drenara la vida misma.

APOLO: Entonces no hay tiempo que perder!
¡A rescatar a Persefone!

Thadeus asiente 

